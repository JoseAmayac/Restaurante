    public void generarReporte()
    {
        Reporte reporte = new Reporte();
        Date fecha1 = new java.sql.Date(this.jDFecha1.getDate().getTime());
        Date fecha2 = new java.sql.Date(this.jDFecha2.getDate().getTime());
        
        int resultado = reporte.consultarVentasTotales(fecha1, fecha2);
        this.jLResultado.setText("Ventas Totales: "+resultado+"");
    }
    
    public DefaultTableModel generarTabla()
    {
        Date fecha1 = new java.sql.Date(this.jDFecha1.getDate().getTime());
        Date fecha2 = new java.sql.Date(this.jDFecha2.getDate().getTime());
        DefaultTableModel modelo;
        String titulo[]={" Numero Mesa "," Ventas "};
        Reporte controlador = new Reporte();
        ArrayList<String[]> res = controlador.consultarVentasMesas(fecha1,fecha2);
        String [][] tabla = new String[res.size()][2];
        for (int i = 0; i < res.size(); i++)
        {
            String[] datos=res.get(i);
            tabla[i]=datos; 
        }
        modelo=new DefaultTableModel(tabla,titulo);
        return modelo;
    }
    
